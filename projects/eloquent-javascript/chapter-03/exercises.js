function countChars(str, n) {
  if (!str) {
    return 0;  
  }
  let count = 0;
  for (var i = 0; i < str.length; i++) {
   console.log('Hit this for loop')
    if (str[i] === n) {
      count += 1;
  }
}
  return count;
}

