////////////////////////////////////////////////////////////////////////////////

function drawChessboard(num) {
  let newBoard = '';
  for (let y = 0; y < num.length; y++) {
    for (let x = 0; x < num.length; x++) {
      if (x + y % 2 === 0) {
        newBoard += ' ';
      } else { 
        newBoard += '#'; 
      }
    }
    newBoard += `\n`;  
  
  } 
  console.log(newBoard); 

}

