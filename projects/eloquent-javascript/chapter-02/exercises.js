////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz(start, end) {
  // print start to end
  // anything that's a multiple of 3 'fizz'
  // if anythign that's a multiple of 5 'buzz'
  // if both 'fizzbuzz'
  for (var i = start; i <= end; i++) {
    if (i % 3 === 0 && i % 5 === 0) {
      console.log('fizzbuzz');

    } else if (i % 3 === 0)  {
      console.log('fizz');
    } else if(i % 5 === 0) {
      console.log('buzz'); 
    } else {
      console.log(i);
    }
  }
}

